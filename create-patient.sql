CREATE TABLE HOSPITAL_MGMT.PATIENT(PATIENT_NAME VARCHAR(100) NOT NULL, 
AGE INT NOT NULL, 
PATIENT_ID NUMERIC NOT NULL, 
NURSE_ID VARCHAR(50), 
DOCTOR_ID VARCHAR(50), 
WARD_NAME VARCHAR(100),
BED_NUMBER INT, 
CUBICLE_NUMBER INT, 
MED_ID INT,
AMOUNT_OF_MED_REQUIRED NUMERIC,
PRIMARY KEY (PATIENT_ID));
